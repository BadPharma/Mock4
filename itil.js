// Function to randomly select questions from each category
function selectQuestions() {
    for (const [category, numQuestions] of Object.entries(questionsPerCategory)) {
        const questions = questionBank[category];
        const shuffled = questions.sort(() => 0.5 - Math.random());
        selectedQuestions.push(...shuffled.slice(0, numQuestions));
    }

    // Shuffle the selected questions to mix them up
    selectedQuestions.sort(() => 0.5 - Math.random());
}

function renderQuiz() {
    const quizContainer = document.getElementById('quizContainer');
    quizContainer.innerHTML = ''; // Clear previous content

    selectedQuestions.forEach((q, index) => {
        const questionCard = document.createElement('div');
        questionCard.className = 'question-card';
        questionCard.style.display = index === 0 ? 'block' : 'none'; // Show only the first card initially

        // Create a container for the flag and question title
        const questionHeader = document.createElement('div');
        questionHeader.className = 'question-header';

        // Create flag checkbox
        const flagCheckbox = document.createElement('input');
        flagCheckbox.type = 'checkbox';
        flagCheckbox.id = `flag${index}`;
        flagCheckbox.name = `flag${index}`;

        // Add event listener to highlight the question when flagged
        flagCheckbox.addEventListener('change', function() {
            if (this.checked) {
                questionCard.style.backgroundColor = 'yellow';
            } else {
                questionCard.style.backgroundColor = ''; // Reset to default
            }
        });

        // Create question title
        const questionTitle = document.createElement('h3');
        questionTitle.textContent = `${index + 1}. ${q.question}`;
        
        // Append checkbox and question title to the question header
        questionHeader.appendChild(flagCheckbox);
        questionHeader.appendChild(questionTitle);

        // Append the question header to the question card
        questionCard.appendChild(questionHeader);

        const optionsElement = document.createElement('div');
        optionsElement.className = 'options';

        q.options.forEach((option, optionIndex) => {
            const optionLabel = document.createElement('label');
            const optionInput = document.createElement('input');
            optionInput.type = 'radio';
            optionInput.name = `question${index}`;
            optionInput.value = option.charAt(0);

            optionLabel.appendChild(optionInput);
            optionLabel.appendChild(document.createTextNode(option));
            optionsElement.appendChild(optionLabel);
        });

        questionCard.appendChild(optionsElement);

        // Navigation buttons
        const navButtons = document.createElement('div');
        navButtons.className = 'nav-buttons';

        if (index > 0) {
            const prevButton = document.createElement('button');
            prevButton.textContent = 'Previous';
            prevButton.addEventListener('click', () => navigateToCard(index - 1));
            navButtons.appendChild(prevButton);
        }

        if (index < selectedQuestions.length - 1) {
            const nextButton = document.createElement('button');
            nextButton.textContent = 'Next';
            nextButton.addEventListener('click', () => navigateToCard(index + 1));
            navButtons.appendChild(nextButton);
        } else {
            // Add the Submit button to the last card
            const submitButton = document.createElement('button');
            submitButton.textContent = 'Submit';
            submitButton.addEventListener('click', submitQuiz);
            navButtons.appendChild(submitButton);
        }

        questionCard.appendChild(navButtons);
        quizContainer.appendChild(questionCard);
    });
}

// Function to navigate between question cards
function navigateToCard(index) {
    const cards = document.querySelectorAll('.question-card');
    cards.forEach((card, i) => {
        card.style.display = i === index ? 'block' : 'none';
    });
}

// Function to handle quiz submission and highlight answers
function submitQuiz() {
    let score = 0;
    let unansweredQuestions = [];

    selectedQuestions.forEach((q, index) => {
        const selectedOption = document.querySelector(`input[name="question${index}"]:checked`);

        if (!selectedOption) {
            // If no option is selected, add the question to the list of unanswered questions
            unansweredQuestions.push(index + 1);
        }
    });

    if (unansweredQuestions.length > 0) {
        // Show an alert listing the unanswered questions
        alert(`Please answer the following questions before submitting: ${unansweredQuestions.join(', ')}`);
    } else {
        // Once all questions are answered, evaluate the answers
        selectedQuestions.forEach((q, index) => {
            const selectedOption = document.querySelector(`input[name="question${index}"]:checked`);
            const options = document.querySelectorAll(`input[name="question${index}"]`);

            options.forEach(option => {
                const optionLabel = option.parentNode;
                // Clear previous styles
                optionLabel.style.color = '';
                optionLabel.style.fontWeight = '';
            });

            if (selectedOption) {
                const selectedLabel = selectedOption.parentNode;
                if (selectedOption.value === q.correctAnswer) {
                    score++;
                    // Highlight correct answer in green
                    selectedLabel.style.color = 'green';
                    selectedLabel.style.fontWeight = 'bold';
                } else {
                    // Highlight incorrect answer in red
                    selectedLabel.style.color = 'red';
                    selectedLabel.style.fontWeight = 'bold';

                    // Find and highlight the correct answer
                    options.forEach(option => {
                        if (option.value === q.correctAnswer) {
                            const correctLabel = option.parentNode;
                            correctLabel.style.color = 'green';
                            correctLabel.style.fontWeight = 'bold';
                        }
                    });
                }
            }
        });

        const resultElement = document.getElementById('result');
        resultElement.textContent = `You scored ${score} out of ${selectedQuestions.length}`;
    }
}

